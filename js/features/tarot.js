  function drawCards() {
    state.activeIdx = 0;
    const count = state.spread;
    const positions = SPREADS[state.spread]?.positions || [];
    state.drawn = sample(TAROT_CARDS, count).map((c, i) => ({
      card: c,
      reversed: state.reversed && Math.random() < 0.3,
      revealed: false,
      position: positions[i] || `第${i + 1}张`
    }));

    // 保存历史
    saveHistory({
      spread: state.spread,
      cards: state.drawn
    });

    renderDrawnCards();
  }

