  function switchTab(tab) {
    state.activeTab = tab;
    // 更新 tab 按钮状态
    document.querySelectorAll('.tarot-tab').forEach(btn => {
      if (btn.dataset.tab === tab) {
        btn.classList.add('tarot-tab-active');
      } else {
        btn.classList.remove('tarot-tab-active');
      }
    });
    // 关闭牌详情
    closeCardDetail();
    // 渲染内容
    if (tab === 'library') {
      renderLibrary();
    } else {
      renderReading();
    }
  }

  // ==================== 模态框控制 ====================
  function ensureModal() {
    let modal = document.getElementById('tarot-modal');
    if (!modal) {
      const wrap = document.createElement('div');
      wrap.innerHTML = getModalHtml();
      document.body.appendChild(wrap.firstElementChild);
      modal = document.getElementById('tarot-modal');

      // 绑定 tab 切换
      modal.querySelectorAll('.tarot-tab').forEach(btn => {
        btn.onclick = () => switchTab(btn.dataset.tab);
      });
    }
    return modal;
  }

  function open() {
    const modal = ensureModal();
    modal.classList.add('tarot-modal-show');
    // 渲染当前 tab
    if (state.activeTab === 'library') {
      renderLibrary();
    } else {
      renderReading();
    }
  }

  function close() {
    const modal = document.getElementById('tarot-modal');
    if (modal) {
      modal.classList.remove('tarot-modal-show');
    }
    closeCardDetail();
  }

