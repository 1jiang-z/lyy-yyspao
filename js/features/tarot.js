  // ==================== 数据解析 ====================
  // 数据格式: 牌名|编号|花色|正位关键词\n正位详细|逆位关键词\n逆位详细|意象|核心|情感|事业|财运|健康|学业|人际|时间|建议|警示|元素|对应星象|数字|一句话
  // 注意: 正位关键词与正位详细在同一个字段中，用 \n 分隔；逆位同理
  function parseTarotData(rawArr) {
    return rawArr.map(str => {
      const parts = str.split('|');
      if (parts.length < 19) {
        console.warn('塔罗牌数据格式错误:', str.substring(0, 20));
        return null;
      }
      // 正位关键词和详细描述用换行分隔
      const upField = parts[3] || '';
      const upLines = upField.split('\n');
      const up = upLines[0] || '';
      const upDetail = upLines.slice(1).join('\n') || '';

      // 逆位关键词和详细描述用换行分隔
      const revField = parts[4] || '';
      const revLines = revField.split('\n');
      const rev = revLines[0] || '';
      const revDetail = revLines.slice(1).join('\n') || '';

      return {
        name: parts[0],
        num: parts[1],
        suit: parts[2],
        up: up,
        upDetail: upDetail,
        rev: rev,
        revDetail: revDetail,
        imagery: parts[5] || '',
        core: parts[6] || '',
        love: parts[7] || '',
        career: parts[8] || '',
        money: parts[9] || '',
        health: parts[10] || '',
        study: parts[11] || '',
        social: parts[12] || '',
        time: parts[13] || '',
        advice: parts[14] || '',
        warn: parts[15] || '',
        ele: parts[16] || '',
        astro: parts[17] || '',
        numv: parts[18] || '',
        phrase: parts[19] || ''
      };
    }).filter(c => c !== null);
  }

